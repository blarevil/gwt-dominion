package com.jeeex.cardgame.shared.entity;

import java.util.HashSet;
import java.util.Set;

import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.OneToOne;

import com.google.gwt.user.client.rpc.IsSerializable;

@Entity
public class GameRoom implements IsSerializable, Cleanable {

	@Id
	@GeneratedValue
	private Long id;

	private String name;

	@OneToOne(optional = false, fetch = FetchType.EAGER)
	private User createdBy;

	@ManyToMany(mappedBy="joinedRooms")
	private Set<User> participating = new HashSet<User>();

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public User getCreatedBy() {
		return createdBy;
	}

	public void setCreatedBy(User createdBy) {
		this.createdBy = createdBy;
	}

	public Set<User> getParticipatingUsers() {
		return participating;
	}

	public void clean() {
		participating = new HashSet<User>(participating);
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		GameRoom other = (GameRoom) obj;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
}
